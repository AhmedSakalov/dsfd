#include <stdio.h>

int		*ft_strlowcase(char *str)
{
	int res;
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			str[i] += 'a' - 'A';
		}
		i++;
	}
	return (str);
}
